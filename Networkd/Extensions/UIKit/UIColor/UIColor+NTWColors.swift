import UIKit

extension UIColor {
    
    class var NTWBlue: UIColor {
        return UIColor(hex: Colors.kNTWBlue, alpha: 1.0)
    }
    
    class var NTWRed: UIColor {
        return UIColor(hex: Colors.kNTWRed, alpha: 1.0)
    }
    
    class var NTWGray: UIColor {
        return UIColor(hex: Colors.kNTWGray, alpha: 1.0)
    }
}
